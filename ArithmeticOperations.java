package Homework;

public class ArithmeticOperations {
    public static void main(String[] args) {
        byte b=127; long l=10000; short s= 80; int i= 500;
        float f = 20; double v=22.5; char alpha='j'; boolean B = true;

        System.out.println(b+l);//addition with byte & long
        System.out.println(s-i);//subtraction with short & int value
        System.out.println(f*v);//multiplication with float & double value
        System.out.println(b/f);//division with byte & float value
        System.out.println(i++);//increment operator
        System.out.println(i--);//decrement operator
        System.out.println(alpha);//print with character value
        System.out.println(!B);//boolean print true or false





    }

}
