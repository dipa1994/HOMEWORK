package Homework;

public class ZeroPosNeg {
    public static void main(String[] args) {
        int Number = 0;

        if (Number > 0) {
            System.out.println("The Number is Positive");
        }
        else if (Number < 0)
        {
            System.out.println("The Number is Negative");
        }
        else
        {
            System.out.println("Zero is neither positive nor negative");
        }

    }

}

